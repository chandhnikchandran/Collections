package Set;

public class Booking {
String ac;
String cot;
String nc;
String wifi;
String nl;
public String getAc() {
	return ac;
}
public void setAc(String ac) {
	this.ac = ac;
}
public String getCot() {
	return cot;
}
public void setCot(String cot) {
	this.cot = cot;
}
public String getNc() {
	return nc;
}
public void setNc(String nc) {
	this.nc = nc;
}
public String getWifi() {
	return wifi;
}
public void setWifi(String wifi) {
	this.wifi = wifi;
}
public String getNl() {
	return nl;
}
public void setNl(String nl) {
	this.nl = nl;
}
}
